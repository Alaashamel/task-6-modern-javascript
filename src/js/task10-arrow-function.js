// Task 10 - Arrow Function

// Regular function
function sumRegular(x, y) {
    return x + y;
}

// Arrow function equivalent
const sum = (x, y) => x + y;

console.log("=== Task 10: Arrow Function ===");

console.log("Regular Function Result:", sumRegular(5, 3));
console.log("Arrow Function Result:", sum(5, 3));
console.log("");
